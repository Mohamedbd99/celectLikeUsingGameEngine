    public static final TileBlueprint[][] DEFAULT_BLUEPRINT = buildBlueprint();

    public static TileBlueprint[][] copyBlueprint() {
        int rows = DEFAULT_BLUEPRINT.length;
        int cols = DEFAULT_BLUEPRINT[0].length;
        TileBlueprint[][] copy = new TileBlueprint[rows][cols];
        for (int row = 0; row < rows; row++) {
            for (int col = 0; col < cols; col++) {
                copy[row][col] = DEFAULT_BLUEPRINT[row][col].copy();
            }
        }
        return copy;
    }

    public record TileBlueprint(int[] frames, float frameDuration) {
        public static TileBlueprint air() {
            return new TileBlueprint(new int[0], DEFAULT_FRAME_DURATION);
        }

        public TileBlueprint copy() {
            return new TileBlueprint(frames.clone(), frameDuration);
        }
    }

    private static TileBlueprint[][] buildBlueprint() {
        int rows = LEGACY_INT_LAYOUT.length;
        int cols = LEGACY_INT_LAYOUT[0].length;
        TileBlueprint[][] map = new TileBlueprint[rows][cols];
        for (int row = 0; row < rows; row++) {
            for (int col = 0; col < cols; col++) {
                map[row][col] = fromLegacyValue(LEGACY_INT_LAYOUT[row][col]);
            }
        }
        return map;
    }

    private static TileBlueprint fromLegacyValue(int value) {
        return switch (value) {
            case 1 -> contiguousBlueprint(WATER_TILE_INDEX, WATER_FRAMES);
            case 2 -> contiguousBlueprint(SOLID_TILE_INDEX, 1);
            default -> TileBlueprint.air();
        };
    }

    private static TileBlueprint contiguousBlueprint(int startIndex, int frames) {
        int[] indices = new int[frames];
        for (int i = 0; i < frames; i++) {
            indices[i] = startIndex + i;
        }
        return new TileBlueprint(indices, DEFAULT_FRAME_DURATION);
    }

    private static int atlasIndex(int row, int col) {
        return row * ATLAS_COLUMNS + col;
    }
